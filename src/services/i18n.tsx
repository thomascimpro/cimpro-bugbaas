const I18nContext = createContext<I18nContextValue>({
  language: "nl",
  setLanguage: () => undefined,
  t: (key) => key,
  tr: (text) => text
});

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>("nl");

  useEffect(() => {
    void AsyncStorage.getItem(storageKey).then((value) => {
      if (value === "nl" || value === "en" || value === "fr") setLanguageState(value);
    });
  }, []);

  const value = useMemo<I18nContextValue>(() => {
    const translate = (key: string, params?: Record<string, string | number>) => interpolate(dictionaries[language][key] ?? nl[key] ?? key, params);
    return {
      language,
      setLanguage: (nextLanguage) => {
        setLanguageState(nextLanguage);
        void AsyncStorage.setItem(storageKey, nextLanguage);
      },
      t: translate,
      tr: (text, params) => translate(exactTextKeys[text] ?? text, params)
    };
  }, [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  return useContext(I18nContext);
}

export function severityLabel(severity: BugSeverity, t: I18nContextValue["t"]): string {
  return t(severityKeys[severity]);
}

export function statusLabel(status: BugStatus, t: I18nContextValue["t"]): string {
  return t(statusKeys[status]);
}

export function rarityLabel(rarity: BugDexRarity, t: I18nContextValue["t"]): string {
  return t(rarityKeys[rarity]);
}

function interpolate(text: string, params?: Record<string, string | number>): string {
  if (!params) return text;
  return Object.entries(params).reduce((current, [key, value]) => current.replaceAll(`{${key}}`, String(value)), text);
}
